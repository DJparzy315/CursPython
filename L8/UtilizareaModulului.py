import math

# date de intrare
num = 25
angle = 30

# calcule
radacina = math.sqrt(num)
factorial = math.factorial(num)
sinus = math.sin(math.radians(angle))  # convertim grade in radiani

# afisare rezultate
print(f"Radacina patrata a {num} este {radacina}")
print(f"Factorialul lui {num} este {factorial}")
print(f"Sinusul unghiului de {angle} grade este {sinus}")
